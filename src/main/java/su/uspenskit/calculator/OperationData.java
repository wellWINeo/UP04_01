package su.uspenskit.calculator;

public class OperationData {
    private double A;
    private double B;
    private char Op;

    public double getA() {
        return A;
    }

    public void setA(double a) {
        A = a;
    }

    public double getB() {
        return B;
    }

    public void setB(double b) {
        B = b;
    }

    public char getOp() {
        return Op;
    }

    public void setOp(char op) {
        Op = op;
    }
}
